            </FONT></STRONG><FONT color=#000000>Paper weights can be very confusing.&nbsp; We would like to offer another very general understanding of paper weights below.&nbsp; This is not technical, but we will associate common weights with everyday items you may come in contact with.&nbsp; Here at paper-papers.com we deal with two basic weights, "<FONT color=#6EAB23>TEXT WEIGHT</FONT>" and "<FONT color=#6EAB23>COVER/CARD WEIGHT</FONT>".&nbsp; These two identifiers are probably the most important factors when you are purchasing a sheet of paper for your project.&nbsp; <FONT color=#6EAB23>TEXT WEIGHT</FONT> comes in varying weights, but will have a touch like a 'letterhead' paper you may have in your office.&nbsp; <FONT color=#6EAB23>COVER WEIGHT</FONT> (CARD WEIGHT) comes in varying weights, but will have a touch like your 'business card' or 'post card' you may have in your office.&nbsp; Keeping these things in mind, we will list some common weights below.&nbsp; <BR>
              </FONT></FONT><font color="#6EAB23"><BR>
          <STRONG>TEXT WEIGHT:</STRONG></font><STRONG><FONT color=#008080><BR>
          </FONT>20LB Text </STRONG> - typically used as all-purpose paper, for copying machines, etc.<BR>
      <STRONG>24/60LB Text </STRONG> - typically used for laser/ink printing and letterheads<BR>
      <STRONG>28/70LB Text</STRONG>- typically used for letterhead and other important documents<BR>
      <STRONG>80LB Text </STRONG> - a bit heavier than letterhead paper<BR>
      (and the weights continue to go up....but this is still 'text weight' paper....don't let the similar weights confuse you with cardstock. Please see table above with additional comparison by using grammage.<BR>
      <BR>
      <FONT color=#008080><STRONG><font color="#6EAB23">COVER WEIGHT:</font></STRONG><BR>
        </FONT><STRONG>57LB</STRONG> Vellum Bristol - typically used as mass mailing postcard (light weight postcard)<BR>
          <STRONG>67LB</STRONG> Vellum Bristol - typically used as postcards<BR>
      <STRONG>65LB</STRONG> Cover - typically used as light weight business cards, greeting cards, post cards, etc.<BR>
      <STRONG>90 LB</STRONG> Index - typically used as light weight business cards, door hangers, post cards, etc.<BR>
      <STRONG>80LB</STRONG> Cover - typically used for business cards, etc.<BR>
      <STRONG>89LB</STRONG> Cover - etc.<BR>
      <STRONG>92LB</STRONG> Cover - etc.<BR>
      <STRONG>105LB</STRONG> Cover - etc.<BR>
      <STRONG>110LB</STRONG> Cover - etc.<BR>
      (and the weights continue to go up, please see table above with additional comparison by using grammage)<BR>
      Hopefully by now this is starting to make sense.&nbsp;&nbsp;<span class="red"><EM>**Very important example, 80LB is listed in our text category and our cover category, but they are completely different paper.&nbsp;**</EM></span><EM><BR>
