<!-- D -->

<tr><td colspan="2" height="17"><span class=catfont><a name="D"></a></span></td></tr> 

<tr><td><b>Dampening</b></td><td>An essential part of the offset printing process whereby rollers distribute a solution to the plate that covers the non-printing area of the plate, repelling ink in those areas. Some newer presses use a waterless ink technology that does not use dampening.</td></tr> 

<tr><td><b>Deboss</b></td><td>To press an image into paper with a die so it extends below the surface. The opposite of emboss where the image is raised above the paper surface.</td></tr> 

<tr><td><b>Deckle edge</b></td><td>The rough or feathered edge of paper when left untrimmed.</td></tr> 

<tr><td><b>Densitometer</b></td><td>An optical device used by printers and photographers to measure and control the density of ink or color.</td></tr> 


<tr><td><b>Density</b></td><td>The degree of tone, weight of darkness or color within a photo or reproduction measured by a densitometer.</td></tr> 

<tr><td><b>Descender</b></td><td>A term that describes that portion of lower case letters that extends below the main body of the letter, as in &quot;p&quot;.</td></tr> 

<tr><td><b>Desktop Publishing</b></td><td>Creating materials to be printed using a personal computer, as opposed to taking non-electronic documents to a commercial printing company to be prepared for printing.</td></tr> 

<tr><td><b>Die Cutting</b></td><td>The process of cutting paper in a shape or design by the use of a wooden die or block in which are positioned steel rules in the shape of the desired pattern.</td></tr> 

<tr><td><b>Digital Proof</b></td><td>Color separation data is digitally stored and then exposed to color photographic paper creating a picture of the final product before it is actually printed with ink.</td></tr> 

<tr><td><b>Dithering</b></td><td>The process of averaging between pixels of different colors. This results in a smoother, blended transition between the edge of two areas rather than a jagged or 'stair-step' appearance. Also a method used on ink jet printers where colors are produced by mixing colored dots in a randomized pattern.</td></tr> 

<tr><td><b>Dot</b></td><td>The smallest individual element of a halftone.</td></tr> 

<tr><td><b>Dot gain</b></td><td>A term used to describe when dots are printing larger than they should.</td></tr> 

<tr><td><b>Drill</b></td><td>The drilling of holes into paper for ring or comb binding.</td></tr>	

<tr><td><b>Drop shadow</b></td><td>A shadow image placed offset behind an image to create the affect of the image lifting off the page.</td></tr> 

<tr><td><b>Dull finish</b></td><td>A semi-gloss finish on paper that is less glossy than gloss and more than matte paper.</td></tr> 

<tr><td><b>Dummy</b></td><td>The preliminary assemblage of copy and art elements to be reproduced in the desired finished product, also called a comp.</td></tr> 

<tr><td><b>Duotone</b></td><td>A two-color halftone reproduction generated from a one color photo.</td></tr> 

<tr><td><b>Dye sublimation</b></td><td>A photographic looking color print created by heating dyes on a substrate instead of using inks. Often used for proofing.</td></tr> 

