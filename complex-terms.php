<!-- E -->

<tr><td colspan="2" height="17"><span class=catfont><a name="E"></a></span></td></tr> 

<tr><td><b>Electronic Proof</b></td><td>A process of generating a prepress proof in which paper is electronically exposed to the color separation negatives and passed through  electrically charged pigmented toners, which adhere electrostatically, resulting in the finished proof.</td></tr> 

<tr><td><b>Embossing</b></td><td>The molding and reshaping of paper by the use of special metal dies and heat, counter dies and pressure, to produce a raised image on the paper surface.</td></tr> 

<tr><td><b>Enamel</b></td><td>Another term for gloss coated paper.</td></tr> 

<tr><td><b>EPS</b></td><td>Encapsulated Post Script. A standard file format used to transfer postscript formatting information between applications.</td></tr> 

<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 

<!-- F -->

<tr><td colspan="2" height="17"><span class=catfont><a name="F"></a></span></td></tr> 

<tr><td><b>Felt side</b></td><td>The smoother side of a sheet in the paper. The wire side is the rougher side of the paper. The difference happens in the papermaking process. The differences are eliminated when papers are gloss or matte coated.</td></tr> 

<tr><td><b>Finish</b></td><td>The surface quality of a paper.</td></tr> 

<tr><td><b>Fit</b></td><td>The registration of the different colors on a printed sheet.</td></tr> 

<tr><td><b>Flexography</b></td><td>A printing method using flexible plates where the image to be printed is higher than the non-printing areas. The inked areas are then contact the material to be printed, transferring the ink from the raised areas to the material. Fast drying inks are usually used in this process. Common uses are the printing of cans and bottles and other non-flat items.</td></tr> 

<tr><td><b>Foil</b></td><td>Then metal sheet that is applied to paper using the foil stamping process. Frequently gold colored, but available in many colors.</td></tr> 

<tr><td><b>Foil Embossing</b></td><td>Stamping a thin sheet of metallic foil onto a sheet of paper and then embossing a pattern under it, creating a three dimensional raised area, usually text or an image.</td></tr> 

<tr><td><b>Foil Stamping</b></td><td>Impressing metallic foil onto paper with a heated die.</td></tr>
	
<tr><td><b>Font</b></td><td>The characters which make up a complete typeface and size.</td></tr>

<tr><td><b>FPO - For Position Only</b></td><td>Low resolution or mockup images used to indicate placement and size in a design, but not intended for final production.</td></tr>

<tr><td><b>Free sheet</b></td><td>Any paper that is free from wood pulp impurities.</td></tr>

<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- G -->

<tr><td colspan="2" height="17"><span class=catfont><a name="G"></a></span></td></tr> 

<tr><td><b>Ganging</b></td><td>The combining of two or more different printing projects on the same sheet of paper.</td></tr> 

<tr><td><b>Gate fold</b></td><td>A three or four panel fold where the two outside panels fold inward to meet in the center. In an open gate fold, there are three panels, the bottom of which is twice the size of the folded panels. In a closed gatefold, there are four panels of roughly equal size where the outer panels are folded inward together.</td></tr> 

<tr><td><b>Gathering</b></td><td>Assembling sheets of paper and signatures into their proper sequence. See also collate.</td></tr> 

<tr><td><b>Ghosting</b></td><td>Also known as gloss ghosting. A condition occurring during the printing process when vapors from drying ink on one side of a press sheet interact chemically with dry ink or blank paper on sheets in contact with or on the reverse side of the same sheet creating unintended faint images.</td></tr> 

<tr><td><b>Grain</b></td><td>Paper fibers lie in a similar direction in a sheet of paper. This direction is called the grain. Printing is usually done so that if folding is required, the fold is done parallel to the grain.</td></tr> 

<tr><td><b>Gravure</b></td><td>A printing process using recessed areas on a metal cylinder that hold the ink.</td></tr> 

<tr><td><b>Gripper</b></td><td>A series of metal fingers that hold each sheet of paper as it passes through a printing press.</td></tr> 

<tr><td><b>Gripper edge</b></td><td>The side of a piece of paper held by the gripper fingers as it passes through a printing press. Nothing can be printed in this area.</td></tr> 

<tr><td><b>Gutter</b></td><td>A blank space or margin between components on a printed piece or press sheet.</td></tr> 


<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- H -->

<tr><td colspan="2" height="17"><span class=catfont><a name="H"></a></span></td></tr> 

<tr><td><b>Halftone</b></td><td>Using small dots to produce the impression of a continuous-tone image. The effect is achieved by varying the dot size and the number of dots per square inch.</td></tr> 

<tr><td><b>Halftone screen</b></td><td>A sheet of film or glass containing ruled right-angled lines, used to translate the full tone of a photo to the halftone dot image required for printing.</td></tr> 

<tr><td><b>Hickey</b></td><td>The effect that occurs when a spec of dust or debris (frequently dried ink) adheres to the printing plate and creates a spot or imperfection in the printing.</td></tr> 

<tr><td><b>Highlights</b></td><td>The lightest tones of a photo, printed halftone or illustration. In the finished halftone, these highlights are represented by the finest dots.</td></tr> 

<tr><td><b>Hot melt</b></td><td>An adhesive used in some binding processes, which requires heat for application.</td></tr> 

<tr><td><b>House sheet</b> </td><td>This is a term that refers to a paper that a printer keeps on hand in their shop.</td></tr> 


<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- I -->

<tr><td colspan="2" height="17"><span class=catfont><a name="I"></a></span></td></tr> 

<tr><td><b>Image area</b></td><td>That portion of a printing plate that carries ink and prints on paper.</td></tr> 

<tr><td><b>Imposition</b></td><td>The correct sequential arrangement of pages that are to be printed, along with all the margins in proper alignment, before producing the plates for printing.</td></tr> 

<tr><td><b>Indicia</b></td><td>An image and/or text pre-printed on mailing envelopes in place of a stamp.</td></tr> 

<tr><td><b>Ink Dry Back</b></td><td>When printed ink colors become lighter or less dense after they have dried on the paper.</td></tr> 

<tr><td><b>Insert</b></td><td> A piece of printed material that is inserted into another piece of printed material, such as a magazine or catalog.</td></tr> 

<tr><td><b>Italic</b></td><td>Text that is used to denote emphasis by slanting the type body forward.</td></tr> 



<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- J -->

<tr><td colspan="2" height="17"><span class=catfont><a name="J"></a></span></td></tr> 

<tr><td><b>Jacket</b></td><td>Or dust jacket. The paper cover sometimes called the "dust cover" of a hardbound book.</td></tr> 

<tr><td><b>Jog</b></td><td>To vibrate a stack of finished pages so that they are tightly aligned for final trimming or binding.</td></tr> 

<tr><td><b>Justification</b></td><td>Adjusting the spacing or hyphenation of words and characters to fill a given line of text from end to end. Sometimes referred to as word spacing.</td></tr> 


<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- K -->

<tr><td colspan="2" height="17"><span class=catfont><a name="K"></a></span></td></tr> 

<tr><td><b>Kerning</b></td><td>The narrowing of space between two letters so that they become closer and take up less space on the page.</td></tr> 

<tr><td><b>Keyline</b></td><td>Lines that are drawn on artwork that indicate the exact placement, shape and size of elements including halftones, illustrations, etc.</td></tr> 

<tr><td><b>Kraft</b></td><td>A coarse unbleached paper used for printing and industrial products.</td></tr> 


<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- L -->

<tr><td colspan="2" height="17"><span class=catfont><a name="L"></a></span></td></tr> 

<tr><td><b>Laid finish</b></td><td>A parallel lined paper that has a handmade look.</td></tr> 

<tr><td><b>Lamination</b></td><td>Applying thin transparent plastic sheets to both sides of a sheet of paper, providing scuff resistance, waterproofing and extended use.</td></tr> 

<tr><td><b>Landscape</b></td><td>A document layout where the width is greater than the height. (the opposite of Portrait)</td></tr> 

<tr><td><b>Layout</b></td><td>A rendition that shows the placement of all the elements, images, thumbnails etc., of a final printed piece.</td></tr> 

<tr><td><b>Leading</b></td><td>Space between lines of type. The distance in points between one baseline and the next.</td></tr> 

<tr><td><b>Letterpress</b></td><td>Printing that utilizes inked raised surfaces, usually type, to create the image.</td></tr> 

<tr><td><b>Letterspacing</b></td><td>The addition of space between typeset letters.</td></tr> 

<tr><td><b>Line copy</b></td><td>Any copy that can be reproduced without the use of a halftone screen.</td></tr> 

<tr><td><b>Linen</b></td><td>A paper that emulates the look and texture of linen cloth.</td></tr> 

<tr><td><b>Lithography</b></td><td>The process of printing that utilizes flat or curved inked surfaces to create the printed images.</td></tr> 

<tr><td><b>Logotype</b></td><td>A personalized type or design symbol for a company or product.</td></tr> 

<tr><td><b>Loupe</b></td><td>A small magnifier used to observe the details on a printed sheet.</td></tr> 


<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- M -->

<tr><td colspan="2" height="17"><span class=catfont><a name="M"></a></span></td></tr> 

<tr><td><b>M weight</b></td><td>The actual weight of 1000 sheets of any given size of paper.</td></tr> 

<tr><td><b>Magenta</b></td><td>One of the four process colors, or CMYK, the M is for magenta. Magenta is a predominately red color with some blue. Magenta, cyan and yellow are also the three subtractive primary colors.</td></tr> 

<tr><td><b>Magnetic black</b></td><td>Black ink containing iron oxides, used for magnetic ink character recognition used for check printing.</td></tr> 

<tr><td><b>Make-ready</b></td><td>Paper that is used in the press set-up process before the printing run actually starts. Or the process of setting up press or bindery equipment to produce a specific product, including setting paper size, ink density, image alignment, fold sizes, etc., in preparation for the actual production run.</td></tr> 

<tr><td><b>Matte finish</b></td><td>A coated paper finish that goes through minimal calendaring.</td></tr> 

<tr><td><b>Metallic Ink</b></td><td>Ink that looks metallic when printed. Made with powdered metal or pigments that look metallic. The most common colors used are gold and silver.</td></tr> 

<tr><td><b>Moir&eacute;</b></td><td>An undesirable halftone pattern produced by the incorrect angles of overprinting halftone screens.</td></tr> 

<tr><td><b>Mottle</b></td><td>A term used to describe spotty or uneven ink absorption.</td></tr> 


