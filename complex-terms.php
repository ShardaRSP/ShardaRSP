<tr><td><b>Text paper</b></td><td>A high quality light weight printing paper.</td></tr> 

<tr><td><b>Thermography</b></td><td>A printing process whereby slow drying ink is applied to paper and, while the ink is still wet, is lightly dusted with a resinous powder. The paper then passes through a heat chamber where the powder melts and fuses with the ink to produce a raised surface.</td></tr> 

<tr><td><b>Tint</b></td><td>A halftone screen that contains all the same sized dots.</td></tr> 

<tr><td><b>Trapping</b></td><td>The overlapping of one color over a different, adjacent color to ensure that no white space is visible where the two colors meet, especially when there are slight variations in the registration of the two colors during the printing process. Or the process of printing wet ink over wet or dry previously printed ink.</td></tr> 

<tr><td><b>Trim marks</b></td><td>Marks placed on the printed sheet to indicate where cuts should be made.</td></tr> 

<tr><td><b>Trim size</b></td><td>The final size of a printed piece after being cut from the sheet of paper that it was printed on.</td></tr> 

<tr><td><b>Typo</b></td><td>A spelling mistake in printed material resulting from a mistake in typing or setting type.</td></tr> 

