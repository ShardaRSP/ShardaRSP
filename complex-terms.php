<tr><td colspan="2" height="17"><span class=catfont><a name="A"></a></span></td></tr> 

<tr><td width="160"><b>A4 Paper</b></td><td>ISO standard paper size 210 x 297mm or 8.3 x 11.7". The common paper size used outside the US in place of 8.5 x 11.</td></tr> 

<tr><td><b>Accordion fold</b></td><td>Folding paper by bending each fold in the opposite direction of the previous fold creating a pleated or accordion effect.</td></tr> 

<tr><td><b>Acid-free paper</b></td><td>A paper containing no acidity or acid producing chemicals that degrades less over time than acidic papers.</td></tr> 

<tr><td><b>Against the Grain</b></td><td>Running a sheet of paper through a printing press at right angles to the grain direction of the paper, as opposed to with the grain. This is usually suboptimal for both press operation and registration of the 4 color process inks. Sometimes called cross grain.</td></tr>

<tr><td><b>Airbrush</b></td><td>A compressed air tool that sprays a fine mist of paint or ink, used in illustration and photo retouching.</td></tr>

<tr><td><b>Anti-aliasing</b></td><td>The process of averaging between pixels of different colors. This results is a smoother, more blended transition between the edge of two areas rather than a distinctly jagged appearance.</td></tr>

<tr><td><b>Aqueous Coating</b></td><td>This clear coating is used to protect your printed pieces. It provides a high-gloss surface that deters dirt and fingerprints. Aqueous coating improves the durability of postcards as they go through the mail, and protects business cards as they ride around in people's pockets. It also looks beautiful on brochures, catalog covers, and stand-alone flyers.</td></tr> 

<tr><td><b>Artwork</b></td><td>The original physical materials, including photos, graphic images, text and other components needed to produce a printed piece. Can also now refer to the electronic or digital components needed for preparing a printed piece for production on a press or copier.</td></tr> 

<tr><td><b>Ascender</b></td><td>Any part of a lower case letter which rises above the main body of the letter such as in &quot;d&quot;, &quot;b&quot; and &quot;h&quot;.</td></tr>

<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- B -->

<tr><td colspan="2" height="17"><span class=catfont><a name="B"></a></span></td></tr> 

<tr><td><b>Backslant</b></td><td>Any type that tilts to the left or backward direction; opposite of italic type.</td></tr>

<tr><td><b>Back Up</b></td><td>How an image on one side of a printed sheet aligns with the image on the other side.</td></tr>

<tr><td><b>Balloon</b></td><td>In an illustration, any line that encircles copy or dialogue.</td></tr> 

<tr><td><b>Base line</b></td><td>The imaginary horizontal line upon which stand capitals, lower case letters, punctuation points, etc.</td></tr> 

<tr><td><b>Basis weight</b></td><td>Basis or basic weight refers to the weight, in pounds, of a ream (500 sheets) of paper cut to a given standard size for that particular paper grade.</td></tr> 

<tr><td><b>Bible paper</b></td><td>A thin but strong paper (opaque), used for bibles and books.</td></tr> 

<tr><td><b>Bindery</b></td><td>A business or department within a printing company that does the cutting, folding, collating, drilling and other finishing operations used on printing projects.</td></tr>

<tr><td><b>Blanket</b></td><td>The rubberized surfaced material secured onto a cylinder onto which the ink is transferred from the plate and then to the paper.</td></tr> 

<tr><td><b>Bleed</b></td><td>Any element that extends up to or past the edge of a printed page.</td></tr> 

<tr><td><b>Blind emboss</b></td><td>A design or bas relief impression that is made without using inks or metal foils.</td></tr>

<tr><td><b>Blocking</b></td><td>When ink or coating causes printed sheets of paper in a pile to stick together, causing damage when they are separated. This is normally caused by not enough anti-offset powder or too much ink, and usually ruins the printed job.</td></tr>

<tr><td><b>Body</b></td><td>In typography, the main shank or portion of a letter character other than the ascenders and descenders.</td></tr> 

<tr><td><b>Bond</b></td><td>A grade of durable writing, printing and typing paper that is erasable and somewhat rigid.</td></tr> 

<tr><td><b>Book Paper</b></td><td>Types of paper usually used for printing books. Book paper text weight and is divided into uncoated or offset paper, and coated paper, which includes matte or gloss coating.</td></tr> 

<tr><td><b>Bounce</b></td><td>Inconsistent positioning of the printed image on the sheets of paper as they travel through a printing press.</td></tr> 

<tr><td><b>Bristol</b></td><td>A board paper of various thicknesses having a smooth finish and used for printing or drawing.</td></tr> 

<tr><td><b>Bulk</b></td><td>A term given to paper to describe its thickness relative to its weight.</td></tr> 

<tr><td><b>Bullet</b></td><td>A boldface square or dot used before a sentence to emphasize its importance.</td></tr> 

<tr> <td width="80%" valign="top" colspan="2" height="17"><hr noshade size=1></td></tr> 


<!-- C -->

<tr><td colspan="2" height="17"><span class=catfont><a name="C"></a></span></td></tr> 

<tr><td><b>C1S and C2S</b></td><td>Acronyms for Coated One Side and Coated Two Sides paper stock. A cover stock with a glossy finish on one side and uncoated on the other, usually between 8pt (.008") and 18pt (.018") in thickness.</td></tr> 

<tr><td><b>Caliper</b></td><td>The measurement of the thickness of paper measured in thousandths of an inch or mils.</td></tr> 

<tr><td><b>Case binding</b></td><td>Books bound using hard board (case) covers.</td></tr> 

<tr><td><b>Carbonless Paper</b></td><td>Paper that is chemically treated to transfer the impression from the first page to the subsequent pages.</td></tr> 

<tr><td><b>Cast coated</b></td><td>A paper that is coated and then pressure dried using a polished roller that imparts an enamel like hard gloss finish.</td></tr> 

<tr><td><b>Center spread</b></td><td>The two pages that face each other in the center of a book or publication.</td></tr> 

<tr><td><b>Chain lines</b></td><td>Lines that appear on laid paper as a result of the wires of the papermaking machine.</td></tr> 
 
<tr><td><b>Clip art</b></td><td>Graphic images, designs, and artwork in digital form that can be used in a digital document.</td></tr> 

<tr><td><b>Coarse screen</b></td><td>Halftone screens commonly used in newsprint; up to 85 lines per inch.</td></tr> 

<tr><td><b>Coated stock</b></td><td>Any paper that has a mineral coating applied after the paper is made, giving the paper a smoother finish.</td></tr> 

<tr><td><b>Coil Binding</b></td><td> Where a metal or plastic wire is spiraled through holes punched along the side of a stack of paper. Commonly used for reports, proposals and manuals. Documents bound with coil have the ability to lay flat and can rotate 360 degrees. Also called spiral binding.</td></tr> 

<tr><td><b>Cold color</b></td><td>Any color that is toward the blue side of the color spectrum.</td></tr> 

<tr><td><b>Collate</b></td><td>To gather sheets or printed signatures together in their correct order.</td></tr> 

<tr><td><b>Colophon</b></td><td>A printers' or publishers' identifying symbol or emblem.</td></tr> 

<tr><td><b>Color balance</b></td><td>The relative amounts of process colors used to reproduce an image, either digitally or when printed on a press.</td></tr> 
	
<tr><td><b>Color bars</b></td><td>A color test strip that is printed on the waste portion of a press sheet. It helps a press operator to monitor and control the quality of the printed material relative to ink density, registration and dot gain. It can also include a Star Target, which is designed to detect inking and press problems.</td></tr> 

<tr><td><b>Color cast</b></td><td>Unwanted color tone or overall color shading distorting the normal color balance of a photographic image.</td></tr> 

<tr><td><b>Color correction</b></td><td>Using a computer to adjust, change or manipulate a color image, such as retouching, adjusting color balance, color saturation, contrast, etc.</td></tr> 

<tr><td><b>Color gamut</b></td><td>The entire range of hues possible to reproduce on a specific system, such as a computer screen, or four-color printing press.</td></tr> 

<tr><td><b>Color separating</b></td><td>The processes of separating the primary color components (CMYK) for printing.</td></tr> 

<tr><td><b>Color Sequence</b></td><td>The order in which process inks are printed on a printing press. Also called the color rotation or laydown sequence.</td></tr> 

<tr><td><b>Color transparency</b></td><td>Transparent film containing a positive photographic color image.</td></tr> 

<tr><td><b>Comb Binding</b></td><td>Binding a stack of paper together by inserting the teeth of a flexible plastic comb into holes punched along one of the edges. Commonly used for catalogs, reports and manuals.</td></tr> 

<tr><td><b>Condensed type</b></td><td>A narrow, elongated typeface.</td></tr> 

<tr><td><b>Contrast</b></td><td>The degree of tonal separation or gradation in the range from black to white.</td></tr> 

<tr><td><b>Cover</b></td><td>A term describing a general type of paper used for the covers of books, pamphlets, etc., also used for business cards and postcards.</td></tr> 

<tr><td><b>Coverage</b></td><td>The extent to which printing ink covers the surface of a printed sheet. Ink coverage is frequently expressed as light, medium or heavy.</td></tr> 

<tr><td><b>Crop</b></td><td>To reduce the size of an image.</td></tr> 

<tr><td><b>Crop marks</b></td><td>Small printed lines around the edges of a printed piece indicating where it is to be cut out of the sheet. Sometimes referred to as cut marks.</td></tr> 

<tr><td><b>Crossover</b></td><td>An image, rule or line art on one printed page that carries over to an adjacent page of a bound or folded work.</td></tr> 

<tr><td><b>Cyan</b></td><td>A shade of blue used in four-color process printing. The C in CMYK. Also referred to as process blue.</td></tr> 


