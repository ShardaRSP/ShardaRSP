<tr><td><b>Rag paper</b></td><td>Papers with a complete or partial content of cotton fibers.</td></tr> 

<tr><td><b>Ragged left</b></td><td>The term given to right-justified type that is uneven on the left.</td></tr> 

<tr><td><b>Ragged right</b></td><td>The term given to left-justified type that is uneven on the right.</td></tr> 

<tr><td><b>Ream</b></td><td>500 sheets of paper.</td></tr> 

<tr><td><b>Register</b></td><td>The arrangement of two or more printed images in exact alignment with each other.</td></tr> 

<tr><td><b>Register marks</b></td><td>Any crossmarks or other symbols used on a press sheet to assure proper registration.</td></tr> 

<tr><td><b>RGB</b></td><td>The color space of Red, Green and Blue. These are the primary colors of light, which computers use to display images on your screen. An RGB computer file must be translated into the CMYK (the primary colors of pigment) color space in order to be printed on a printing press.</td></tr> 

<tr><td><b>Right angle fold</b></td><td>A term that denotes folds that are 90 degrees to each other.</td></tr> 

<tr><td><b>Running head</b></td><td>A title at the top of a page that appears on all pages of a book or chapter of a book.</td></tr> 


