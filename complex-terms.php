<!-- P -->

<tr><td colspan="2" height="17"><span class=catfont><a name="P"></a></span></td></tr> 

<tr><td><b>Pagination</b></td><td>The numbering of individual pages in a multi-page document</td></tr> 

<tr><td><b>Parchment</b></td><td>A hard finished paper that emulates animal skin used for documents, such as awards, that require writing by hand.</td></tr> 

<tr><td><b>Parent sheet</b></td><td>A sheet that is larger than the cut stock of the same paper.</td></tr> 

<tr><td><b>Perfect Binding</b></td><td>A binding process where the signatures of a book are held together by a flexible adhesive.</td></tr> 

<tr><td><b>Perfecting press</b></td><td>A printing press that prints on both sides of a sheet in a single pass through the press.</td></tr> 

<tr><td><b>Pica</b></td><td>A typesetting unit of measurement equaling 1/6th of an inch.</td></tr> 

<tr><td><b>Picking</b></td><td>An occurrence in printing whereby the tack of ink pulls fibers or coating off the paper surface, leaving spots on the printed surface.</td></tr> 

<tr><td><b>Plastic comb</b></td><td>A method of binding books whereby holes are drilled on the spine, and a plastic grasping device is inserted to hold the pages together.</td></tr> 

<tr><td><b>PMS</b></td><td>The abbreviation of the Pantone Color Matching System.</td></tr> 

<tr><td><b>Point</b></td><td>A measurement unit equal to 1/72 of an inch. 12 points to a pica, 72 points to an inch.</td></tr> 

<tr><td><b>Portrait</b></td><td>A document layout in which the height is greater than the width. (the opposite of Landscape)</td></tr> 

<tr><td><b>PostScript</b></td><td>A tradename of Adobe Systems, Inc. for its page description language. This language translates a digital file from an application into a language a compatible printer or other device can use to create its output.</td></tr> 

<tr><td><b>Ppi</b></td><td>Pages per inch or pixels per inch.</td></tr> 

<tr><td><b>Premium</b></td><td>Any paper that is considered better than grade #1 by its manufacturer.</td></tr> 

<tr><td><b>Press Check</b></td><td>When a client visits a printing company to view actual printed sheets of their project before a full production press run is started.</td></tr> 

<tr><td><b>Pressure-sensitive</b></td><td>Self-adhesive paper covered by a backing sheet.</td></tr> 

<tr><td><b>Process printing</b></td><td>A system where a color image is separated into different color values (cyan, magenta, yellow and black or CMYK) by the use of filters and screens or digitally with a software program and then transferred to printing plates and printed on a printing press, reproducing the original color image.</td></tr> 

<tr><td><b>Progressive proofs</b></td><td>Any proofs made from the separate colors of a multi-color printing project.</td></tr> 


