<tr><td colspan="2" height="17"><span class=catfont><a name="S"></a></span></td></tr> 

<tr><td><b>Saddle stitch</b></td><td>The binding of booklets or other printed materials by stapling the pages on the folded spine.</td></tr> 

<tr><td><b>Safety paper</b></td><td>A paper that shows sign of erasure so that it cannot be altered or tampered with easily.</td></tr> 

<tr><td><b>Scoring</b></td><td>To crease paper with a metal rule for the purpose of making folding easier.</td></tr> 

<tr><td><b>Screen angles</b></td><td>The placement of halftone screens to avoid unwanted moir&eacute; patterns. Frequently used angles are black 45&ordm;, magenta 75&ordm;, yellow 90&ordm;, and cyan 105&ordm;.</td></tr> 

<tr><td><b>Screen ruling</b></td><td>A measurement equaling the number of lines or dots per inch on a halftone screen.</td></tr> 

<tr><td><b>Scum</b></td><td>Unwanted deposits of ink in the non-image area of a printed piece.</td></tr> 

<tr><td><b>Self cover</b></td><td>A cover that is the same paper stock as the internal sheets.</td></tr> 

<tr><td><b>Sharpen</b></td><td>To decrease the dot size of a halftone, which in turn decreases the color strength.</td></tr> 

<tr><td><b>Sheetwise</b></td><td>The printing of two different images on two different sides of a sheet of paper by turning the sheet over after the first side is printed and using the same gripper and side guides.</td></tr> 

<tr><td><b>Show through</b></td><td>When the printing on one side of a sheet is seen from the other side, a frequent problem with thin papers.</td></tr> 

<tr><td><b>Side guide</b></td><td>The guides on the sides of a printing press that consistently positions the sheet sideways as it is fed through the press.</td></tr> 

<tr><td><b>Side stitch</b></td><td>The stapling of sheets or signatures on the side closest to the spine.</td></tr> 

<tr><td><b>Signature</b></td><td>A printed sheet with multiple pages on it that is folded so that the pages are in their proper numbered sequence, as in a book.</td></tr> 

<tr><td><b>Smoothness</b></td><td>That quality of paper defined by its levelness that allows for pressure consistency in printing, assuring uniformity of print.</td></tr> 

<tr><td><b>Soy Inks</b></td><td>Inks made with soy oils instead of petroleum as the base. They are considered to be more environmentally friendly, a standard component of green printing.</td></tr> 

<tr><td><b>Spiral bind</b></td><td>A type of binding where a metal or plastic wire is spiraled through holes drilled along the binding side of a document.</td></tr> 

<tr><td><b>Stock</b></td><td>A term for unprinted paper.</td></tr> 

<tr><td><b>Super calendaring</b></td><td>A machine procedure that produces a very smooth paper surface that is exceptional for printing.</td></tr> 

<tr><td><b>Synthetic papers</b></td><td>Any non-wood or cloth paper, usually petroleum (plastic) based.</td></tr> 


