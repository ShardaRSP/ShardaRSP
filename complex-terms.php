<!-- O -->

<tr><td colspan="2" height="17"><span class=catfont><a name="O"></a></span></td></tr> 

<tr><td><b>Offset</b></td><td>An erroneous variation of the word "setoff". Ink that is unintentionally transferred from a printed sheet to the back of the sheet above it as the pieces are stacked in a pile when printed.</td></tr> 

<tr><td><b>Offset printing</b></td><td>The most commonly used printing method, where the printed material does not receive ink directly from a printing plate but from an intermediary blanket that receives the ink from the plate and then transfers it to the paper.</td></tr> 

<tr><td><b>Offset paper</b></td><td>A term for sometimes used for uncoated book paper.</td></tr> 

<tr><td><b>Onionskin</b></td><td>A light bond paper used for typing and used with carbon paper because of its thinness.</td></tr> 

<tr><td><b>Opacity</b></td><td>Quality of papers that defines its opaqueness or ability to prevent two-sided printing from showing through.</td></tr> 

<tr><td><b>Opaque ink</b></td><td>Ink that completely covers any ink under itself.</td></tr> 

<tr><td><b>Overlay proof</b></td><td> A process of proof-making whereby the color separations are individually exposed to light sensitive film. This film is then set in registration with a piece of white paper in the background.</td></tr> 

<tr><td><b>Overprinting</b></td><td>Any printing that is done on an area that has already been printed.</td></tr> 

<tr><td><b>Overrun</b></td><td>Quantities of sheets printed over the requested number of copies.</td></tr> 


